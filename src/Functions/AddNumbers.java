package Functions;
// number shows which line will execute first.
public class AddNumbers {
    static void add(){   // '3'
        int num1=10;     // '4'
        int num2=20;     // '5'
        int sum=num1+num2;   //  '6'
        System.out.println("Sum is:" +sum);   // '7'
    }  // '8'
    public static void main(String[] args) {    //'1'
        add();   // '2'
    }   // '9'
}  // '10'
